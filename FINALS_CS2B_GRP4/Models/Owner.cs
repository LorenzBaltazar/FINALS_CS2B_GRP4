public class Owner
{
    // OwnerId property represents the unique identifier of the owner
    public int OwnerId { get; set; }

    // FirstName property represents the first name of the owner
    public string FirstName { get; set; }

    // LastName property represents the last name of the owner
    public string LastName { get; set; }

    // Address property represents the address of the owner
    public string Address { get; set; }

    // PhoneNumber property represents the phone number of the owner
    public string PhoneNumber { get; set; }

    // Email property represents the email address of the owner
    public string Email { get; set; }
}
